// Fetch current Kalshi prediction-market prices for all 12 group-winner markets
// and the World Cup outright winner market, then write to src/data/marketOdds.ts.
// Run manually (or via cron) before each deploy to refresh the Markets-mode picks.
//
//   node scripts/fetch-kalshi-odds.mjs
//
// Kalshi public API: no auth required for market data.

import fs from "node:fs"

// Extract the 48 WC team names from teams.ts via regex so we don't need ts-node.
const teamsSrc = fs.readFileSync("src/data/teams.ts", "utf8")
const WC_TEAM_NAMES = [...teamsSrc.matchAll(/name:\s*"([^"]+)"/g)].map((m) => m[1])

const GROUPS = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j", "k", "l"]
const BASE = "https://api.elections.kalshi.com/trade-api/v2/markets"

// Kalshi sometimes uses a slightly different spelling than our teams.ts.
// Map Kalshi's yes_sub_title -> our team name (must match teams.ts exactly).
const NAME_FIX = {
  "USA": "United States",
  "Czechia": "Czech Republic",
  "Türkiye": "Turkey",
  "Turkiye": "Turkey",
  "Korea": "South Korea",
  "Korea Republic": "South Korea",
  "Republic of Korea": "South Korea",
  "IR Iran": "Iran",
  "Iran (Islamic Republic of)": "Iran",
  "Democratic Republic of Congo": "DR Congo",
  "DR of Congo": "DR Congo",
  "Congo DR": "DR Congo",
  "Congo": "DR Congo",
  "Côte d'Ivoire": "Ivory Coast",
  "Cote d'Ivoire": "Ivory Coast",
  "Curacao": "Curaçao",
  "Cabo Verde": "Cape Verde",
  "Saudi Arabia (KSA)": "Saudi Arabia",
}

// Only accept teams that are actually in our 48-team WC field. Kalshi sometimes
// lists alternate-qualification scenarios (Ireland, Denmark, etc.) that don't apply.
const VALID_TEAMS = new Set(WC_TEAM_NAMES)

const sleep = (ms) => new Promise((r) => setTimeout(r, ms))

async function fetchJson(url) {
  const res = await fetch(url, { headers: { "Accept": "application/json" } })
  if (!res.ok) throw new Error(`${url} -> HTTP ${res.status}`)
  return res.json()
}

// Each group event has 4 binary YES/NO markets, one per team. The yes price
// (last_price_dollars) is the implied probability that team wins the group.
async function fetchGroup(letter) {
  const data = await fetchJson(`${BASE}?event_ticker=KXWCGROUPWIN-26${letter.toUpperCase()}&limit=50`)
  const out = {}
  for (const m of data.markets ?? []) {
    const raw = m.yes_sub_title?.trim()
    if (!raw) continue
    const team = NAME_FIX[raw] ?? raw
    if (!VALID_TEAMS.has(team)) continue   // drop alternate-scenario teams
    const price = parseFloat(m.last_price_dollars ?? "0")
    out[team] = price
  }
  return out
}

// Outright winner: one event with ~48 binary markets, one per team.
async function fetchOutright() {
  const data = await fetchJson(`${BASE}?event_ticker=KXMENWORLDCUP-26&limit=80`)
  const out = {}
  for (const m of data.markets ?? []) {
    const raw = m.yes_sub_title?.trim()
    if (!raw) continue
    const team = NAME_FIX[raw] ?? raw
    if (!VALID_TEAMS.has(team)) continue
    const price = parseFloat(m.last_price_dollars ?? "0")
    out[team] = price
  }
  return out
}

const groupOdds = {}
for (const g of GROUPS) {
  const teamsForGroup = await fetchGroup(g)
  Object.assign(groupOdds, teamsForGroup)
  await sleep(350) // rate-limit guard for Kalshi public API
  const summary = Object.entries(teamsForGroup)
    .sort(([, a], [, b]) => b - a)
    .map(([t, p]) => `${t} ${(p * 100).toFixed(0)}%`)
    .join(", ")
  console.log(`Group ${g.toUpperCase()}: ${summary}`)
}

const championOdds = await fetchOutright()
const topChamp = Object.entries(championOdds)
  .sort(([, a], [, b]) => b - a)
  .slice(0, 10)
  .map(([t, p]) => `${t} ${(p * 100).toFixed(1)}%`)
  .join(", ")
console.log(`\nChampion top 10: ${topChamp}`)

// Pin a snapshot timestamp so the page can show "last updated".
const fetchedAt = new Date().toISOString()

const ts = `// Auto-generated by scripts/fetch-kalshi-odds.mjs. Do not edit by hand.
// Source: Kalshi public market data (KXWCGROUPWIN-26* + KXMENWORLDCUP-26).
// Values are implied probabilities (0.0 to 1.0) from each market's last trade price.

/** Probability of finishing first in the group, keyed by team name (must match teams.ts). */
export const groupWinnerOdds: Record<string, number> = ${JSON.stringify(groupOdds, null, 2)}

/** Probability of winning the tournament outright, keyed by team name. */
export const championOdds: Record<string, number> = ${JSON.stringify(championOdds, null, 2)}

/** When this snapshot was taken . display on the page so users know it's not live. */
export const oddsFetchedAt = "${fetchedAt}"
`

fs.writeFileSync("src/data/marketOdds.ts", ts)
console.log(`\nWrote src/data/marketOdds.ts (${Object.keys(groupOdds).length} group entries, ${Object.keys(championOdds).length} champion entries)`)
